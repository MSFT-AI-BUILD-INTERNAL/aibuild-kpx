"""F7 — Render benchmark-results.html section 07b from live aggregate JSONs.

Reads R7/R8/R9 aggregates + per-model JSONLs, emits the HTML fragment that
populates the "라이브 API 벤치마크" panel. Intended to be re-runnable so that
adding new rounds only requires updating the inputs list.

Usage:
    PYTHONPATH=. python -m bench_real.render_results_panel \
        --inputs r7=bench_real/runs/lite-r7-xjudge \
                 r8=bench_real/runs/lite-r8-retry-v2 \
                 r9=bench_real/runs/lite-r9-deepseek \
        --out bench_real/runs/_panel_07b.html

The script is read-only with respect to docs/. To inject the fragment into
docs/benchmark-results.html, copy the contents of the output file between the
``<!-- panel:07b:start -->`` and ``<!-- panel:07b:end -->`` markers.
"""
from __future__ import annotations
import argparse
import json
from pathlib import Path
from typing import Iterable


def _load_per_variant(runs_dir: Path) -> dict[str, dict[str, dict[str, float]]]:
    """Return {model: {variant: {q, tok_in, n}}} from per-model JSONLs."""
    out: dict[str, dict[str, dict[str, float]]] = {}
    for f in sorted(runs_dir.glob("*.jsonl")):
        rows = [json.loads(line) for line in f.read_text().splitlines()
                if line.strip()]
        if not rows:
            continue
        model = rows[0].get("model", f.stem)
        by_v: dict[str, list] = {}
        for r in rows:
            by_v.setdefault(r["variant"], []).append(r)
        per_v: dict[str, dict[str, float]] = {}
        for v, rs in by_v.items():
            ok = [r for r in rs if not r.get("error")]
            if not ok:
                continue
            n = len(ok)
            q = sum(r["quality_score"] for r in ok) / n
            ti = sum(r.get("tokens_in_api") or 0 for r in ok) / n
            # F9: count rule_based judge fallbacks for transparency.
            fb = sum(1 for r in ok
                     if (r.get("judge_axes") or {}).get("fallback")
                     == "rule_based")
            per_v[v] = {"q": q, "tok_in": ti, "n": n, "fb": fb}
        if per_v:
            out[model] = per_v
    return out


def _render(rounds: dict[str, dict], out_path: Path) -> None:
    """Render an HTML fragment with V0→V4 savings bars sorted descending."""
    # Aggregate V0 vs V4 per model across all rounds.
    rows: list[dict] = []
    for round_id, models in rounds.items():
        for model, per_v in models.items():
            if "V0" not in per_v or "V4" not in per_v:
                continue
            ti0, ti4 = per_v["V0"]["tok_in"], per_v["V4"]["tok_in"]
            q0, q4 = per_v["V0"]["q"], per_v["V4"]["q"]
            if ti0 <= 0:
                continue
            save = (ti0 - ti4) / ti0 * 100.0
            rows.append({
                "round": round_id,
                "model": model,
                "save": save,
                "dq": q4 - q0,
                "n_v0": per_v["V0"]["n"],
                "n_v4": per_v["V4"]["n"],
                "fb": int(per_v["V0"].get("fb", 0))
                      + int(per_v["V4"].get("fb", 0)),
            })
    rows.sort(key=lambda r: -r["save"])

    lines = ["<!-- panel:07b:start (auto-generated by "
             "bench_real.render_results_panel) -->",
             '<div class="panel tight">']
    for r in rows:
        sign = "+" if r["dq"] >= 0 else ""
        bar_w = min(max(r["save"], 0.0), 100.0)
        fb_tag = f", fb={r['fb']}" if r["fb"] else ""
        lines.append(
            f'  <div class="bar-row"><span class="label">{r["model"]}</span>\n'
            f'    <div class="bar"><span style="width:{bar_w:.2f}%"></span></div>\n'
            f'    <span class="val pos" style="color:var(--ok)">'
            f'+{r["save"]:.1f}% / Δq {sign}{r["dq"]:.1f} '
            f'(n={r["n_v0"]}/{r["n_v4"]}{fb_tag}, {r["round"]})</span></div>'
        )
    lines.append("</div>")
    lines.append("<!-- panel:07b:end -->")
    fragment = "\n".join(lines) + "\n"
    out_path.write_text(fragment, encoding="utf-8")
    print(f"[render] {len(rows)} rows → {out_path}")
    for r in rows:
        fb_s = f"  fb={r['fb']}" if r["fb"] else ""
        print(f"  {r['round']:>3}  {r['model']:<42}  "
              f"save={r['save']:+.2f}%  Δq={r['dq']:+.2f}  "
              f"n={r['n_v0']}/{r['n_v4']}{fb_s}")
    return fragment


START_MARK = "<!-- panel:07b:start"
END_MARK = "<!-- panel:07b:end -->"


def _markers(panel: str) -> tuple[str, str]:
    """F11: derive (start_prefix, end_full) markers from a panel id.

    The start marker is matched as a prefix (so the auto-generated annotation
    after ``start`` is allowed). The end marker is matched exactly.
    """
    return (f"<!-- panel:{panel}:start", f"<!-- panel:{panel}:end -->")


def _patch_doc(doc_path: Path, fragment: str, panel: str = "07b",
               backup: bool = False, dry_run: bool = False,
               diff_out: Path | None = None) -> None:
    """F8: replace marker-bracketed region in doc_path with fragment.

    Atomic write via *.tmp + os.replace. Idempotent: re-running produces the
    same file when inputs are unchanged.

    F10: when ``backup`` is True, write ``<doc>.bak`` (overwriting prior backup)
    before replacing. F11: ``panel`` selects the marker pair.
    F13: when ``dry_run`` is True, print a unified diff and do not write.
    F16: when ``diff_out`` is set, also write the diff to that path (works
    alongside dry-run or real patch).
    """
    import os
    start_mark, end_mark = _markers(panel)
    text = doc_path.read_text(encoding="utf-8")
    s = text.find(start_mark)
    if s < 0:
        raise SystemExit(f"start marker {start_mark!r} not found in {doc_path}")
    e = text.find(end_mark, s)
    if e < 0:
        raise SystemExit(f"end marker {end_mark!r} not found in {doc_path}")
    e_end = e + len(end_mark)
    new_text = text[:s] + fragment.rstrip("\n") + text[e_end:]

    diff_text = ""
    if dry_run or diff_out is not None:
        import difflib
        diff_text = "".join(difflib.unified_diff(
            text.splitlines(keepends=True),
            new_text.splitlines(keepends=True),
            fromfile=f"{doc_path} (current)",
            tofile=f"{doc_path} (proposed)",
            n=2,
        ))
    if diff_out is not None:
        diff_out.write_text(diff_text, encoding="utf-8")
        print(f"[diff] → {diff_out} ({len(diff_text)} bytes)")
    if dry_run:
        if diff_text:
            print(diff_text, end="" if diff_text.endswith("\n") else "\n")
        else:
            print(f"[dry-run] {doc_path} panel={panel}: no change")
        return
    if backup:
        bak = doc_path.with_suffix(doc_path.suffix + ".bak")
        bak.write_text(text, encoding="utf-8")
        print(f"[patch] backup → {bak}")
    tmp = doc_path.with_suffix(doc_path.suffix + ".tmp")
    tmp.write_text(new_text, encoding="utf-8")
    os.replace(tmp, doc_path)
    print(f"[patch] {doc_path} panel={panel} "
          f"({len(fragment)} bytes injected)")


def _list_panels(doc_path: Path) -> list[str]:
    """F14: return panel ids discovered via ``<!-- panel:<id>:start`` markers."""
    import re
    text = doc_path.read_text(encoding="utf-8")
    ids = re.findall(r"<!--\s*panel:([A-Za-z0-9_\-]+):start", text)
    # de-dup while preserving order
    seen: dict[str, None] = {}
    for i in ids:
        seen.setdefault(i, None)
    return list(seen)


def _parse_inputs(items: Iterable[str]) -> dict[str, Path]:
    out: dict[str, Path] = {}
    for item in items:
        k, _, v = item.partition("=")
        if not v:
            raise SystemExit(f"--inputs must be ROUND=PATH (got {item!r})")
        out[k] = Path(v)
    return out


def main(argv: list[str] | None = None) -> int:
    ap = argparse.ArgumentParser(prog="bench_real.render_results_panel")
    ap.add_argument("--inputs", nargs="+",
                    help="space-separated ROUND=PATH entries")
    ap.add_argument("--out", type=Path)
    ap.add_argument("--patch-doc", type=Path, default=None,
                    help="if set, replace marker-bracketed region in this "
                         "HTML doc with the rendered fragment")
    ap.add_argument("--panel", default="07b",
                    help="marker id used by --patch-doc (default: 07b)")
    ap.add_argument("--backup", action="store_true",
                    help="write <doc>.bak before patching")
    ap.add_argument("--dry-run", action="store_true",
                    help="print unified diff for --patch-doc and exit "
                         "without writing")
    ap.add_argument("--diff-out", type=Path, default=None,
                    help="also write unified diff to this path")
    ap.add_argument("--list-panels", type=Path, default=None,
                    help="list panel ids in given HTML doc and exit")
    args = ap.parse_args(argv)
    if args.list_panels:
        ids = _list_panels(args.list_panels)
        for i in ids:
            print(i)
        return 0
    if not args.inputs or not args.out:
        raise SystemExit("--inputs and --out are required unless "
                         "--list-panels is used")
    inputs = _parse_inputs(args.inputs)
    rounds = {k: _load_per_variant(p) for k, p in inputs.items()}
    fragment = _render(rounds, args.out)
    if args.patch_doc:
        _patch_doc(args.patch_doc, fragment, panel=args.panel,
                   backup=args.backup, dry_run=args.dry_run,
                   diff_out=args.diff_out)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
